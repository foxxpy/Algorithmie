#Fonctionnement de la méthode isalpha() issue des chaînes de caractères.
#Cette méthode teste si tous les caractères d'une chaîne de caractères sont
#des lettres de l'alphabet.

pseudonyme1 = "Foxxpy"
pseudonyme2 = "Foxxpy68"
prenom = "Félix"
phrase = "Bonjour. Vous allez bien?"

print(pseudonyme1.isalpha()) #True
print(pseudonyme2.isalpha()) #False
print(prenom.isalpha()) #True
print(phrase.isalpha()) #False
