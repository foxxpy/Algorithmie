#Déclaration des variables
string_avec_majuscule_et_minuscule = "Bonjour à tous!"
string_majuscule = "BONJOUR A TOUS!"
string_minuscule = "bonjour à tous!"
string_ponctuation = "Bonjour! Vous allez bien? Moi... oui!"
string_chiffre = "J'ai 3 chats. Et vous?"

#Mise en minuscule avec lower()
print("----- LOWER -----")

print(string_avec_majuscule_et_minuscule.lower())
print(string_majuscule.lower())
print(string_minuscule.lower())
print(string_ponctuation.lower())
print(string_chiffre.lower())


#Mise en majuscule avec upper()
print("\n----- UPPER -----")

print(string_avec_majuscule_et_minuscule.upper())
print(string_majuscule.upper())
print(string_minuscule.upper())
print(string_ponctuation.upper())
print(string_chiffre.upper())
